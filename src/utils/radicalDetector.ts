export function getRadicalsForChar(char: string): string[] {
  return RADICAL_MAP[char] || [];
}

export function hasRadical(char: string, radical: string): boolean {
  const radicals = getRadicalsForChar(char);
  return radicals.includes(radical);
}

export function getRadicalColor(radical: string): string {
  const radicalInfo = RADICALS.find(r => r.radical === radical);
  return radicalInfo?.color || '#6b7280';
}

export function getRadicalName(radical: string): string {
  const radicalInfo = RADICALS.find(r => r.radical === radical);
  return radicalInfo?.name || radical;
}
